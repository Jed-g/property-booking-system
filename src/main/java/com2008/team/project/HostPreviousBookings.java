    private void fetchPreviousData(String email) {
        
        previousPage.setEnabled(false);
        nextPage.setEnabled(false);
        
        Date dateToday = new Date(new java.util.Date().getTime());
        
        previousList = HostBookingList.getPreviousList(email, dateToday);
        
        int n = previousList.length;
        
        if (n <= 4){
            removePreviousBoxes(4-n);
        } else {
            nextPage.setEnabled(true);
        }
        
        if (n <= 4) {
            
            numberOfPages = 1;
            
        } else if (n%4 == 0) {
            
            numberOfPages = n%4;
            nextPage.setEnabled(true);
            
        } else {
            numberOfPages = (n%4) + 1;
            nextPage.setEnabled(true);
        }
        
        pageNumber.setText("1/" + numberOfPages);
        
        fillPreviousBoxes(0);
        
    }
    
    private void fillPreviousBoxes(int indexFirstPreviousOnPage) {
        
        int maxAmountBoxes = 4;
        
        if (indexFirstPreviousOnPage + 4 > previousList.length){
            maxAmountBoxes = previousList.length - indexFirstPreviousOnPage;
        }
        if (maxAmountBoxes >= 1){
            propName1.setText(previousList[indexFirstPreviousOnPage].getPropertyName());
            propLocation1.setText(previousList[indexFirstPreviousOnPage].getLocation());
            dateRange1.setText(previousList[indexFirstPreviousOnPage].getDateRange());
            guestName1.setText(previousList[indexFirstPreviousOnPage].getGuestName());
        }
        if (maxAmountBoxes >= 2){
            propName2.setText(previousList[indexFirstPreviousOnPage +1].getPropertyName());
            propLocation2.setText(previousList[indexFirstPreviousOnPage +1].getLocation());
            dateRange2.setText(previousList[indexFirstPreviousOnPage +1].getDateRange());
            guestName2.setText(previousList[indexFirstPreviousOnPage +1].getGuestName());
        }
        if (maxAmountBoxes >= 3){
            propName3.setText(previousList[indexFirstPreviousOnPage +2].getPropertyName());
            propLocation3.setText(previousList[indexFirstPreviousOnPage +2].getLocation());
            dateRange3.setText(previousList[indexFirstPreviousOnPage +2].getDateRange());
            guestName3.setText(previousList[indexFirstPreviousOnPage +2].getGuestName());
        }
        if (maxAmountBoxes == 4){
            propName4.setText(previousList[indexFirstPreviousOnPage +3].getPropertyName());
            propLocation4.setText(previousList[indexFirstPreviousOnPage +3].getLocation());
            dateRange4.setText(previousList[indexFirstPreviousOnPage +3].getDateRange());
            guestName4.setText(previousList[indexFirstPreviousOnPage +3].getGuestName());
        }
        
    }
    
    private void removePreviousBoxes(int numBoxesToBeRemoved) {
        
        if (numBoxesToBeRemoved >= 1){
            propName4.setVisible(false);
            propLocation4.setVisible(false);
            dateRange4.setVisible(false);
            guestName4.setVisible(false);
        }
        if (numBoxesToBeRemoved >= 2){
            propName3.setVisible(false);
            propLocation3.setVisible(false);
            dateRange3.setVisible(false);
            guestName3.setVisible(false);
        }
        if (numBoxesToBeRemoved >= 3){
            propName2.setVisible(false);
            propLocation2.setVisible(false);
            dateRange2.setVisible(false);
            guestName2.setVisible(false);
        }
        if (numBoxesToBeRemoved == 4){
            propName1.setVisible(false);
            propLocation1.setVisible(false);
            dateRange1.setVisible(false);
            guestName1.setVisible(false);
        }
        
    }
    
